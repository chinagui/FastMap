/**
 * FM-A04-12-01	POI中文名称长度检查	DHM	
 * 检查条件：
 * Lifecycle！=1（删除）；
 * 检查原则： 
 * 名称（name）字段大于35个字符（不区分全半角，只计算字符个数，汉字也属于1个字符），需要报出。
 * 提示：POI中文名称长度检查：POI中文名称长度不能大于35个字符
 * @author zhangxiaoyi
 *
 */
public class FMA041201 extends BasicCheckRule {

	@Override
	public void runCheck(BasicObj obj) throws Exception {
		if(obj.objName().equals(ObjectName.IX_POI)){
			IxPoiObj poiObj=(IxPoiObj) obj;
			IxPoi poi=(IxPoi) poiObj.getMainrow();
			IxPoiName nameObj = poiObj.getOfficeOriginCHName();
			if(nameObj==null){return;}
			String nameStr = nameObj.getName();
			if(nameStr==null||nameStr.isEmpty()){return;}
			if(nameStr.length()>35){
				setCheckResult(poi.getGeometry(), poiObj, poi.getMeshId(), null);
				return;
			}
		}
	}

	@Override
	public void loadReferDatas(Collection<BasicObj> batchDataList)
			throws Exception {
	}

}
